# Integer
data = 42
print(type(data))

# Float
data = 3.14
print(type(data))

# String
data = "Hello"
print(type(data))

# Boolean
data = True
print(type(data))

# List
data = [1, 2, 3]
print(type(data))

# Tuple
data = (1, 2, 3)
print(type(data))

# Dictionary
data = {"a": 1, "b": 2}
print(type(data))

# Set
data = {1, 2, 3}
print(type(data))

# NoneType
data = None
print(type(data))